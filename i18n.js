function translateRuntimeText(message, lang) {
  if (typeof message !== "string" || !message) return message || "";

  const exactKey = EXACT_RUNTIME_KEYS[message];
  if (exactKey) return t(exactKey, lang);

  for (const entry of RUNTIME_PATTERNS) {
    const match = message.match(entry.pattern);
    if (match) {
      return t(entry.key, lang, entry.map(match));
    }
  }

  return message;
}

