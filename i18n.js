function normalizeLang(lang) {
  return lang === "ar" ? "ar" : "en";
}

function isRtlLang(lang) {
  return normalizeLang(lang) === "ar";
}

function getTranslations(lang) {
  return TRANSLATIONS[normalizeLang(lang)] || TRANSLATIONS.en;
}

function t(key, lang, replacements) {
  const normalizedLang = normalizeLang(lang);
  const strings = getTranslations(normalizedLang);
  let str = strings[key] || TRANSLATIONS.en[key] || key;
  if (replacements) {
    for (const [placeholder, value] of Object.entries(replacements)) {
      str = str.replace(new RegExp(`\\{${placeholder}\\}`, "g"), String(value));
    }
  }
  return str;
}

